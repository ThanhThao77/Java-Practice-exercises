    public void getLoadData(){
        header.add("height");
        header.add("radius");
        header.add("color");
       
      
        
        XLExam xl = new XLExam();
        ArrayList<Cylinder> kh = xl.getTT();
        for (int i = 0; i < kh.size(); i++) {
            Cylinder cb = (Cylinder) kh.get(i);
            Vector tbVector = new Vector();
            tbVector.add(cb.getHeight());
            tbVector.add(cb.getRadius());
            tbVector.add(cb.getColor());

            datas.add(tbVector);
        }
        tbExam.setModel(new DefaultTableModel(datas, header));
    }
    

