    void click(int selectedRow){
        Vector vtSelected = (Vector)datas.get(selectedRow);
        String Height = vtSelected.get(0).toString();
        String Radius = vtSelected.get(1).toString();
        
        txtHeight.setText(Height);
        txtRadius.setText(Radius);
        String Color = vtSelected.get(2).toString();
        
        cbColor.setSelectedItem(Color);
        
    }
    
