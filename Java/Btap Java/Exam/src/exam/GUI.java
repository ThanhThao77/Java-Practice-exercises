    public void getloadData() {
        ArrayList ls = xl.getdata();
        for (int i = 0; i < ls.size(); i++) {
            Rectangle re = (Rectangle) ls.get(i);
            Vector tb = new Vector();
            tb.add(re.getWidlth());
            tb.add(re.getLength());
            tb.add(re.getColor());
            if (re.isFilled()) {
                tb.add("Dc to mau");
            } else {
                tb.add("ko dc to mau");
            }
            tb.add(re.getWidlth() * re.getLength());
            tb.add((re.getWidlth() * re.getLength()) * 2);
            row.add(tb);
        }
        model.setDataVector(row, col);
        jTable1.setModel(model);
    }

